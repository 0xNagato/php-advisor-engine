## Here's how PRIMA works:

Within the App’s Menu,
<mark>there are two places where you can book dining reservations</mark>
: The Availability Calendar and Reservation Hub.

- **The Availability Calendar** shows you real-time availability of tables at all of our participating venues.
- **The Reservation Hub** is used only for bookings at one specific venue (when you know exactly what you want to
book and when).

There are two types of restaurant reservations: **PRIME** and **NON-PRIME**.

### PRIME RESERVATIONS:

PRIME reservations are usually at peak times, and restaurants tend to be fully sold out on traditional booking
platforms. In order to accommodate the guest at their chosen time, <mark>the guest pays a **“Platform Access Fee”**</mark> to
PRIMA to secure this reservation on their behalf. The fee is clearly shown during the booking process to both the
guest and concierge. Our dedicated team communicates directly with the restaurant to accommodate the guest, and we
transfer 60% of the fee paid to the restaurant.

<mark>**Important:** The PRIME Fee is not applicable towards restaurant spend, it is strictly for PRIMA to secure this
reservation for the guest.</mark>

PRIMA shares up to 15% of the fee with the concierge submitting the booking.

#### Here is how much you can earn from PRIME reservations:

- 10% of the Platform Access Fee for the first 10 monthly reservations
- 15% of the Platform Access Fee for bookings 11 and above.

<mark>All commissions are calculated on a monthly basis and reset at the end of the month.</mark>

### NON-PRIME RESERVATIONS:

NON-PRIME reservations never have a fee. While some restaurants are always sold out, many have tables available
during non-peak times. We call these “NON-PRIME” reservations. <mark>Restaurants pay PRIMA an Incentive Fee for these
bookings, and we share up to 80% of this Fee with the referring concierge</mark>. You can see exactly how much you can earn
from each booking by visiting the “Earnings Potential” tab.

#### Here is how much you can earn from NON-PRIME reservations:

- 70% of the Restaurant Incentive for the first 10 monthly reservations
- 80% of the Restaurant Incentive for bookings 11 and above.

#### Example:

If the restaurant is paying PRIMA $10 per diner, and you are booking a reservation for 6 guests, the total amount
earned is $60, and you will receive up to 80% of this rate, or $48 for the booking (So long as the customer does not
cancel or no show). We pay only for “seated” bookings.

<mark>PRIMA and our restaurant partners pay close attention to no-shows and cancellations. If your account exceeds our
network average, booking ability may be suspended.</mark>

## REFERRALS:

In addition to revenue sharing on bookings, PRIMA offers a two-tier referral structure for concierge referrals.

**Here's how it works:**

We reward our concierges for referring other hospitality professionals to the platform.

1. Our network is invitation only. As such, no concierges can sign up for PRIMA without being referred by a
concierge who already has access.
2. It is imperative that we maintain a high level of quality and trust with our restaurant and partners. As such,
please be mindful of whom you invite to PRIMA.
3. No consumers should be invited to have access to PRIMA via a concierge account, they should be provided with a
VIP Link/Code if the concierge wishes for them to have access. VIP Codes are available to generate within the
platform.

For all bookings made through PRIMA, the platform generates revenue. <mark>We share a percentage of this revenue with all
referring concierges.</mark>

### There are two levels of referrals: **PRIMARY** and **SECONDARY**.

#### PRIMARY (Level 1)

If you refer a concierge to the platform, and this concierge creates a booking through PRIMA, you will earn 10% of
the Platform Revenue that PRIMA generates on this booking.

- **NO FEE (Non Prime)** example: If the incentive fee paid by the restaurant is $20 and the concierge you referred
is earning $14, there is $6 of “Platform Revenue” left to be shared. You will earn 10% of this fee, or $0.60.
- **PAID (PRIME)** example: If the platform access fee paid by the customer is $200, the restaurant receives 60% of
this fee. If the concierge earns 10%, this leaves $60 in "Platform Revenue” to be shared. As such, you will earn 10%
of this fee, or $6.

#### SECONDARY (Level 2)

For any referrals made by your referrals, you will earn 5% of Platform Revenue instead of 10% as in Level 1.

