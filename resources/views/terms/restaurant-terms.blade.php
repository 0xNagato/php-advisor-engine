<p dir="ltr" class="font-black">
    RESTAURANT AGREEMENT
</p>

<p dir="ltr">
    This Restaurant Agreement (this “Agreement”) is entered into by and among
    [PRIMA] LLC, a Florida limited liability company (“PRIMA”), and the
    restaurant accepting this Agreement (“Restaurant”).
</p>
<p dir="ltr">
    PRIMA connects customers with prime-time reservations at in-demand
    restaurants (“Reservations”) via PRIMA’s platform (the “Platform”). The
    Restaurant desires to allow PRIMA to sell Reservations for the Restaurant on
    the Platform.
</p>

<p dir="ltr">
    Please read this Agreement carefully before you start to use the Platform.
    By creating an account to use the Platform and/or by clicking to accept or
    agree to this Agreement when this option is made available to you either via
    the Platform or email, Restaurant accepts and agrees to be bound and abide
    by this Agreement.
</p>

<p dir="ltr">
    1. Definitions.
</p>

<p dir="ltr">
    (a) “Gross Reservation Fee” means the Reservation Fee less any
    applicable credit card or payment processor fees and any refunds.
</p>

<p dir="ltr">
    (b) “Qualified Reservation” means a Reservation at the Restaurant made
    by a customer on the Platform.
</p>

<p dir="ltr">
    (c) “Reservation Fee” means the amount PRIMA charges a customer for a
    Reservation.
</p>

<p dir="ltr">
    2. Commission. Subject to Section 3 below, PRIMA shall pay Restaurant a
    monthly commission (the “Commission”) in the amount equal to [60]% of the
    Gross Reservation Fees for Qualified Reservations for each calendar month.
</p>

<p dir="ltr">
    3. Commission Payment Terms.
</p>

<p dir="ltr">
    (a) Commissions become payable to Restaurant only at such times and only
    to the extent that PRIMA actually receives payment from customer for a
    Qualified Reservation.
</p>

<p dir="ltr">
    (b) PRIMA will provide real-time access to a statement of total
    Commission payable to Restaurant for a calendar month. PRIMA shall remit one
    aggregate payment of the total Commissions to Restaurant within fifteen (15)
    days after the end of the applicable calendar month. PRIMA shall make all
    Commission payments in US dollars via bank wire, check or any other method
    of payment agreed to by the parties.
</p>

<ol start="3">
    <li dir="ltr">
        <p dir="ltr">
            Notwithstanding anything contained in this Agreement to the
            contrary, no Commission is payable under this Agreement if
            Restaurant is in breach of this Agreement. Restaurant acknowledges
            that PRIMA may set Reservation Fees and refund customers in their
            sole discretion.
        </p>
    </li>
</ol>

<p dir="ltr">
    4. Termination. This Agreement commences on the date of this Agreement
    and continues until the termination of this Agreement by either party. Any
    earned but unpaid Commission will be remitted to Restaurant within sixty
    (60) days after termination of this Agreement.
</p>

<p dir="ltr">
    5. Reservations. Restaurant will provide PRIMA with Reservations.
    Restaurant will ensure that the customer is provided seating at the time
    allotted in the Reservation, as in the event a Restaurant’s handling of the
    Reservation is not satisfactory to the customer, PRIMA may have to refund
    customer such applicable Reservation Fees.
</p>

<p dir="ltr">
    6. Miscellaneous.
</p>

<p dir="ltr">
    (a) Assignment. Restaurant may not assign this Agreement, in whole or in
    part, without the consent of PRIMA. PRIMA may assign this Agreement.
</p>

<p dir="ltr">
    (b) Severability. The invalidity or unenforceability of any provision
    hereof shall in no way affect the validity or enforceability of any other
    provision.
</p>

<p dir="ltr">
    (c) Amendments and Waivers. Any term of this Agreement may be amended,
    terminated or waived only with the written consent of PRIMA and the
    Restaurant.
</p>

<p dir="ltr">
    (d) Governing Law and Dispute Resolution. This Agreement shall be
    governed by and construed in accordance with the laws of Florida. All
    disputes arising out of or in connection with this Agreement shall be
    exclusively brought in [Miami], Florida.
</p>

<p dir="ltr">
    (e) Notices. Any notices to be given hereunder shall be deemed to have
    been properly given if sent by email or overnight courier, to the parties
    hereto as set forth on the Platform, or in Restaurant’s account information,
    or at such other email address or address as either party may advise the
    other in writing.
</p>

<p dir="ltr">
    (f) Entire Agreement. This Agreement constitutes the full and entire
    understanding and agreement between the parties with respect to the entire
    subject matter hereof.
</p>
